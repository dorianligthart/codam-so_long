#include <stddef.h>

void	ft_mapchr(char **map, char c, size_t *x, size_t *y)
{
	*x = 0;
	*y = 0;
	while (map[*y])
	{
		while (map[*y][*x])
		{
			if (map[*y][*x] == c)
				break ;
			x++;
		}
		y++;
	}
}